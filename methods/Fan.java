package methods;

public class Fan {
	
		public static void turnOn() {
			System.out.println("turning on the fan");
		}
		public static void increaseSpeed() {
			System.out.println("increase the speed the fan");
		}
		public static void decreseSpeed() {
			System.out.println("decrese the speed of the fan");
		}
		public static void turnoff() {
			System.out.println("turn of the fan");
		}

	public static void main(String[] args) {
		Fan.turnOn();
		Fan.increaseSpeed();
		Fan.decreseSpeed();
		Fan.turnoff();
		

	}

}
